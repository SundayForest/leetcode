    public static int[] searchRange(int[] nums, int target) {
        int[] res = new int[]{-1,-1};
        int low = 0;
        int high = nums.length - 1;
        int point = low + (high - low) / 2;
        while (low <= high){
            if(high - low < 10){
                for (int i = low; i <= high;i++){
                    if(nums[i] == target){
                        res[0] = i;
                        break;
                    }
                }
                break;
            }
            if(nums[point] == target){
                if(point - low < 10){
                    for(int i = low; i <= point; i++){
                        if(nums[i] == target){
                            res[0] = i;
                            break;
                        }
                    }
                    break;
                }
                if(point > 0){
                    if(nums[point - 1] != target){
                        res[0] = point;
                        break;
                    }else{
                        point = point - (point - low) / 2;
                    }
                }else{
                    res[0] = point;
                }
            }else if(nums[point] < target){
                low = point;
                point = low + (high - low) / 2;
            }else{
                high = point;
                point = low + (high - low) / 2;
            }
        }
        if(res[0] != -1){
            low = res[0];
            point = low + (high - low) / 2;
            while (low <= high){
                if(high - low < 10){
                    for (int i = high; i >= low;i--){
                        if(nums[i] == target){
                            res[1] = i;
                            break;
                        }
                    }
                    break;
                }
                if(nums[point] == target){
                    if(high - point < 10){
                        for(int i = high; i >= point; i--){
                            if(nums[i] == target){
                                res[1] = i;
                                break;
                            }
                        }
                        break;
                    }
                    if(point < nums.length - 1){
                        if(nums[point + 1] != target){
                            res[1] = point;
                            break;
                        }else{
                            point = point + (high - point) / 2;
                        }
                    }else{
                        res[1] = point;
                    }
                }else{
                    high = point;
                    point = low + (point - low) / 2;
                }
            }
        }
        return res;
    }

